    private void jTextField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField2ActionPerformed
        // TODO add your handling code here:
        String text = this.jTextField2.getText();

        Helper.cleanTree(jTree1);
        ((DefaultTreeModel) jTree1.getModel()).reload();


        try {
            Helper.loadPluginsOnTree(jTree1, PluginsManager.getLocalPluginsByFilter(text,
                    Helper.treeToVector(jTree3, false)));
        } catch (Exception e) {
            e.printStackTrace();
        }
        Helper.expandAll(jTree1, true);
}//GEN-LAST:event_jTextField2ActionPerformed

