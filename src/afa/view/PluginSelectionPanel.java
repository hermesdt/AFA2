    Vector<Category> getSelectedPlugins() {
        return selectedPlugins;
    }

    boolean checkPanel() throws Exception {

        Vector<Category> vec = Helper.treeToVector(jTree3, true);
        for (Category c : vec) {
            for (Plugin p : c.getPlugins()) {
                selectedPlugins = vec;
                return true;
            }
        }

        javax.swing.JOptionPane.showMessageDialog(null, "Debes seleccionar al menos un plugin",
                "No plugin selected",
                javax.swing.JOptionPane.ERROR_MESSAGE);

        return false;

    }
    

    void init() throws Exception {
        Helper.cleanTree(jTree1);
        Helper.cleanTree(jTree3);
        Helper.loadPluginsOnTree(jTree1, null);
        Helper.expandAll(jTree1, true);
    }

